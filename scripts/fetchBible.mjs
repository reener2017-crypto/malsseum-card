async function fetchVerse(bookNum, chapter, verse) {
  const url = `${BASE}/${bookNum}/${chapter}.json`;
  const res = await fetch(url);
  if (!res.ok) return null;
  const data = await res.json();
  const verseText = data?.verses?.[verse]?.text ?? data?.verses?.[String(verse)]?.text;
  if (!verseText) return null;
  return {
    book: BOOK_NAMES[bookNum],
    chapter,
    verse,
    text: verseText.trim().replace(/\s+/g, " "),
  };
}

async function main() {
  const results = [];
  const chapterCache = {};

  for (const [bookNum, chapter, verse] of POPULAR_VERSES) {
    const key = `${bookNum}-${chapter}`;
    try {
      if (!chapterCache[key]) {
        const url = `${BASE}/${bookNum}/${chapter}.json`;
        const res = await fetch(url);
        if (res.ok) chapterCache[key] = await res.json();
        await new Promise(r => setTimeout(r, 120));
      }
      const data = chapterCache[key];
      const v = data?.verses?.[verse] ?? data?.verses?.[String(verse)];
      if (v?.text) {
        results.push({
          id: results.length + 1,
          book: BOOK_NAMES[bookNum],
          chapter,
          verse,
          text: v.text.trim().replace(/\s+/g, " "),
        });
        process.stdout.write(`✓ ${BOOK_NAMES[bookNum]} ${chapter}:${verse}\n`);
      }
    } catch (e) {
      process.stdout.write(`✗ ${bookNum}:${chapter}:${verse} - ${e.message}\n`);
    }
  }

  const outPath = path.resolve("data/bible.json");
  fs.writeFileSync(outPath, JSON.stringify(results, null, 2), "utf8");
  console.log(`\n완료! 총 ${results.length}개 구절 → data/bible.json`);
}

main();
